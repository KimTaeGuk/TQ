package DTO;

public class RegisterBean {
	private String register_id;
	private String register_pw;
	private String register_name;
	private String register_year;
	private String register_day;
	private String register_month;
	private String register_email;
	
	
	public String getRegister_id() {
		return register_id;
	}
	public void setRegister_id(String register_id) {
		this.register_id = register_id;
	}
	public String getRegister_pw() {
		return register_pw;
	}
	public void setRegister_pw(String register_pw) {
		this.register_pw = register_pw;
	}
	public String getRegister_name() {
		return register_name;
	}
	public void setRegister_name(String register_name) {
		this.register_name = register_name;
	}
	public String getRegister_year() {
		return register_year;
	}
	public void setRegister_year(String register_year) {
		this.register_year = register_year;
	}
	public String getRegister_day() {
		return register_day;
	}
	public void setRegister_day(String register_day) {
		this.register_day = register_day;
	}
	public String getRegister_month() {
		return register_month;
	}
	public void setRegister_month(String register_month) {
		this.register_month = register_month;
	}
	public String getRegister_email() {
		return register_email;
	}
	public void setRegister_email(String register_email) {
		this.register_email = register_email;
	}

}
