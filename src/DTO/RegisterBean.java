package DTO;

public class RegisterBean {
	private String registerId;
	private String registerPw;
	private String registerName;
	private String registerBirth;
	private String registerImg;
	
	public String getRegisterId() {
		return registerId;
	}
	public void setRegisterId(String registerId) {
		this.registerId = registerId;
	}
	public String getRegisterPw() {
		return registerPw;
	}
	public void setRegisterPw(String registerPw) {
		this.registerPw = registerPw;
	}
	public String getRegisterName() {
		return registerName;
	}
	public void setRegisterName(String registerName) {
		this.registerName = registerName;
	}
	public String getRegisterBirth() {
		return registerBirth;
	}
	public void setRegisterBirth(String registerBirth) {
		this.registerBirth = registerBirth;
	}
	public String getRegisterImg() {
		return registerImg;
	}
	public void setRegisterImg(String registerImg) {
		this.registerImg = registerImg;
	}
}
